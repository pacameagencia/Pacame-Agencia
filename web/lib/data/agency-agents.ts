export function getAgentsByDivision(divisionId: string): AgencyAgent[] {
  return agencyAgents.filter(a => a.division === divisionId);
}

export function getAgentsByParent(parentId: string): AgencyAgent[] {
  return agencyAgents.filter(a => a.pacameParent === parentId);
}

export function searchAgents(query: string): AgencyAgent[] {
  const q = query.toLowerCase();
  return agencyAgents.filter(a =>
    a.name.toLowerCase().includes(q) ||
    a.specialty.toLowerCase().includes(q) ||
    a.whenToUse.toLowerCase().includes(q) ||
    a.divisionLabel.toLowerCase().includes(q)
  );
}
